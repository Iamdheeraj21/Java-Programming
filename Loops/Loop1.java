import java.util.*;

class Loop1
{
    public static void main(String[] args) {
        Scanner scanner=new Scanner(System.in);
        int n=scanner.nextInt();
        for (int i = 0; i <n; i++) {
            for(int j=0;j<j;j++){
                System.out.println("*");
            }
            System.out.println("\n");
        }
        scanner.close();
    }
}